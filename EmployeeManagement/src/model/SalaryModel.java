package model;

public class SalaryModel {
	private int sid;
	private String username;
	
	private int netamount;
	private int bsalary;
	
	private int othour;
	 private int otAmount;
	 private int etf;
	 
	private int epf;
	
	 private String sdate;
	public String getSdate() {
		return sdate;
	}

	public void setSdate(String sdate) {
		this.sdate = sdate;
	}

	public int getNetamount() {
		return netamount;
	}

	public void setNetamount(int netamount) {
		this.netamount = netamount;
	}

	public int getSid() {
		return sid;
	}

	public void setSid(int sid) {
		this.sid = sid;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public int getBsalary() {
		return bsalary;
	}

	public void setBsalary(int bsalary) {
		this.bsalary = bsalary;
	}

	public int getOthour() {
		return othour;
	}

	public void setOthour(int othour) {
		this.othour = othour;
	}

	public int getOtAmount() {
		return otAmount;
	}

	public void setOtAmount(int otAmount) {
		this.otAmount = otAmount;
	}

	public int getEtf() {
		return etf;
	}

	public void setEtf(int etf) {
		this.etf = etf;
	}

	public int getEpf() {
		return epf;
	}

	public void setEpf(int epf) {
		this.epf = epf;
	}

	
}
